#include "Constants.h"

const double Constants::massPion    = 0.1395706;   // GeV/c^2
const double Constants::massProton  = 0.938272;    // GeV/c^2
const double Constants::massK0      = 0.497611;    // GeV/c^2
const double Constants::massLambda0 = 1.115683;    // GeV/c^2

const double Constants::mPion(){
	return massPion;
}
const double Constants::mProton(){
	return massProton;
}
const double Constants::mK0(){
	return massK0;
}
const double Constants::mLambda0(){
	return massLambda0;
}
